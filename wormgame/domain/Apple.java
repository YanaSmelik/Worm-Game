package wormgame.domain;

public class Apple extends Piece {


    public Apple(int x, int y, int width) {
        super(x, y, width);
    }

    @Override
    public int getX() {
        return super.getX();
    }

    @Override
    public int getY() {
        return super.getY();
    }

    @Override
    public boolean runsInto(Piece piece) {
        return super.runsInto(piece);
    }

    @Override
    public String toString() {
        return super.toString();
    }

}
